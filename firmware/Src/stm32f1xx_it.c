// SYSTICK fires every 1ms
volatile uint8_t sigPulseTimeout = 0;
void HAL_SYSTICK_Callback()
{
  // heartbeat LED
  HAL_GPIO_WritePin(LED_BLUE_GPIO_Port, LED_BLUE_Pin, ((HAL_GetTick() % 5000 < 50) ? 1 : 0) );

  // The input capture system will not set sigPulseWidth/Period if it receives no pulses at all.
  // So, we have to do that here, notgint hat the IC interrupt will reset sigPulseTimer each
  // time it does detect a pulse.
  if (sigPulseTimeout++ > SIG_MAX_PERIOD)
  {
    sigPulseTimeout--;
    sigPulseWidth = sigPulsePeriod = (-1);
  }

  if (activityTimer) --activityTimer; // millisecond down counter
  if (auxTimer) --auxTimer; // millisecond down counter
  if (MS5611_Timer) MS5611_Timer--; // millisecond downcounter for MS5611 ADC conversion delay
  
  static uint8_t spiTimer = 0;
  if (spiTimer++ == 16) // 62.5 frames per second should be fine
  {
    spiTimer = 0;
    spiStateMachine();
  }

  morseStateMachine();
  lostModelStateMachine();

}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  uint16_t tp;
  if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) // TIM4
  {
    sigPulseTimeout = 0;  
    tp = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1) >> 1;
    sigPulseWidth = (tp < 3000) ? tp : (-1); // reject pulse widths > 3ms
  }
  else if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) // TIM4
  {
    sigPulseTimeout = 0;  
    tp = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2) >> 1;
    if (tp < 23500) sigPulsePeriod = tp; // reject periods < 23ms maximum old-school frame rate, plus margin. 
                                         // (9ms is typical for a Fr-Sky receiver channel)
    else sigPulsePeriod = sigPulseWidth = (-1); // invalidate width also in this case
  } 
}

